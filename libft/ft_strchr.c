char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	ret;

	i = 0;
	ret = (char)c;
	while (s[i] != '\0')
	{
		if (ret == s[i])
		{
			return ((char*)&s[i]);
		}
		i++;
	}
	if (ret == '\0')
	{
		return ((char*)&s[i]);
	}
	return (0);
}
